/**
 * Izhaar — Cloudinary Bulk Image Uploader
 * ----------------------------------------
 * Run: node scripts/upload-to-cloudinary.mjs
 *
 * Uploads all WebP images from src/assets to Cloudinary
 * and generates a URL map file you can import in your app.
 *
 * BEFORE RUNNING:
 *   Set your Cloudinary API Key & Secret below (from cloudinary.com → Settings → API Keys)
 */

import { v2 as cloudinary } from 'cloudinary';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

// ─── CONFIG — Fill these in from cloudinary.com → Settings → API Keys ───────
const CLOUD_NAME = 'df5jbm55b';
const API_KEY = process.env.CLOUDINARY_API_KEY || 'YOUR_API_KEY';
const API_SECRET = process.env.CLOUDINARY_API_SECRET || 'YOUR_API_SECRET';
// ─────────────────────────────────────────────────────────────────────────────

cloudinary.config({ cloud_name: CLOUD_NAME, api_key: API_KEY, api_secret: API_SECRET, secure: true });

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ASSETS_DIR = path.join(__dirname, '../src/assets');
const OUTPUT_FILE = path.join(__dirname, '../src/cloudinaryUrls.js');

// Collect all .webp files recursively
function getAllWebp(dir, base = dir, results = []) {
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
        const full = path.join(dir, entry.name);
        if (entry.isDirectory()) {
            getAllWebp(full, base, results);
        } else if (entry.name.endsWith('.webp')) {
            results.push({ full, relative: path.relative(base, full) });
        }
    }
    return results;
}

const files = getAllWebp(ASSETS_DIR);
console.log(`\n📦 Found ${files.length} WebP images to upload...\n`);

const urlMap = {};
let uploaded = 0;
let skipped = 0;
let failed = 0;

for (const { full, relative } of files) {
    // Convert path to a Cloudinary public_id: e.g. "services/songs"
    const publicId = 'izhaar/' + relative.replace(/\\/g, '/').replace('.webp', '');

    try {
        const result = await cloudinary.uploader.upload(full, {
            public_id: publicId,
            overwrite: false,       // skip if already uploaded
            resource_type: 'image',
            format: 'webp',
            transformation: [{ quality: 'auto', fetch_format: 'auto' }],
        });

        // Build optimized CDN URL
        const cdnUrl = cloudinary.url(publicId, {
            fetch_format: 'auto',
            quality: 'auto',
            secure: true,
        });

        urlMap[relative.replace(/\\/g, '/')] = cdnUrl;
        uploaded++;
        console.log(`  ✅ ${relative}`);

    } catch (err) {
        if (err?.error?.message?.includes('already exists')) {
            // Already uploaded — just build the URL
            const cdnUrl = cloudinary.url(publicId, { fetch_format: 'auto', quality: 'auto', secure: true });
            urlMap[relative.replace(/\\/g, '/')] = cdnUrl;
            skipped++;
            console.log(`  ⏭️  ${relative} (already exists)`);
        } else {
            failed++;
            console.error(`  ❌ ${relative}: ${err?.error?.message || err.message}`);
        }
    }
}

// Write the URL map as a JS file you can import anywhere
const jsContent = `// AUTO-GENERATED by scripts/upload-to-cloudinary.mjs
// Cloudinary CDN URLs — use these instead of local imports for fast loading
// Format: f_auto,q_auto = auto format + quality per device

const cloudinaryUrls = ${JSON.stringify(urlMap, null, 2)};

export default cloudinaryUrls;

// Helper: get URL by filename (e.g. getCloudUrl('services/songs.webp'))
export function getCloudUrl(relativePath) {
  return cloudinaryUrls[relativePath] || null;
}
`;

fs.writeFileSync(OUTPUT_FILE, jsContent);

console.log(`
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
✅ Done!
   Uploaded : ${uploaded}
   Skipped  : ${skipped} (already on Cloudinary)
   Failed   : ${failed}

📄 URL map saved to: src/cloudinaryUrls.js

📌 HOW TO USE in your components:
   import { getCloudUrl } from '../cloudinaryUrls';
   <img src={getCloudUrl('services/songs.webp')} loading="lazy" />
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
`);
