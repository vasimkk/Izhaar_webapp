/**
 * Izhaar — Cloudinary Bulk Video Uploader
 * Run: node --env-file=.env scripts/upload-videos-to-cloudinary.mjs
 */

import { v2 as cloudinary } from 'cloudinary';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

cloudinary.config({
    cloud_name: process.env.CLOUDINARY_CLOUD_NAME || 'df5jbm55b',
    api_key: process.env.CLOUDINARY_API_KEY,
    api_secret: process.env.CLOUDINARY_API_SECRET,
    secure: true,
});

const ASSETS_DIR = path.join(__dirname, '../src/assets');
const OUTPUT_FILE = path.join(__dirname, '../src/cloudinaryVideoUrls.js');

function getAllVideos(dir, base = dir, results = []) {
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
        const full = path.join(dir, entry.name);
        if (entry.isDirectory()) {
            getAllVideos(full, base, results);
        } else if (['.mp4', '.webm', '.mov'].includes(path.extname(entry.name).toLowerCase())) {
            results.push({ full, relative: path.relative(base, full) });
        }
    }
    return results;
}

const files = getAllVideos(ASSETS_DIR);
console.log(`\n🎬 Found ${files.length} videos to upload...\n`);

const urlMap = {};
let uploaded = 0, skipped = 0, failed = 0;

for (const { full, relative } of files) {
    const nameNoExt = path.basename(relative, path.extname(relative));
    const subFolder = path.dirname(relative).replace(/\\/g, '/');
    const publicId = subFolder === '.'
        ? `izhaar/video/${nameNoExt}`
        : `izhaar/video/${subFolder}/${nameNoExt}`;

    const sizeMB = (fs.statSync(full).size / 1024 / 1024).toFixed(1);
    console.log(`  ⬆️  ${relative} (${sizeMB}MB)...`);

    try {
        const result = await cloudinary.uploader.upload(full, {
            public_id: publicId,
            resource_type: 'video',
            overwrite: false,
        });

        const cdnUrl = `https://res.cloudinary.com/df5jbm55b/video/upload/q_auto/v${result.version}/${publicId}`;
        urlMap[relative.replace(/\\/g, '/')] = cdnUrl;
        uploaded++;
        console.log(`  ✅ ${nameNoExt}`);

    } catch (err) {
        const msg = err?.error?.message || err.message || '';
        if (msg.toLowerCase().includes('already') || err?.error?.http_code === 400) {
            const cdnUrl = `https://res.cloudinary.com/df5jbm55b/video/upload/q_auto/v1/${publicId}`;
            urlMap[relative.replace(/\\/g, '/')] = cdnUrl;
            skipped++;
            console.log(`  ⏭️  Already exists`);
        } else {
            failed++;
            console.error(`  ❌ ${msg}`);
        }
    }
}

const jsContent = `// AUTO-GENERATED by scripts/upload-videos-to-cloudinary.mjs
// Cloudinary CDN Video URLs — q_auto optimized

const cloudinaryVideoUrls = ${JSON.stringify(urlMap, null, 2)};

export default cloudinaryVideoUrls;

export function getCloudVideoUrl(relativePath) {
  return cloudinaryVideoUrls[relativePath] || null;
}
`;

fs.writeFileSync(OUTPUT_FILE, jsContent);

console.log(`
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
✅ Videos Done!
   Uploaded : ${uploaded}
   Skipped  : ${skipped}
   Failed   : ${failed}
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
`);
